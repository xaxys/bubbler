type KotlinGenerator struct {
	*gen.GenDispatcher
	GenCtx  *gen.GenCtx
	Warning definition.TopLevelWarning
}

func NewKotlinGenerator() *KotlinGenerator {
	g := &KotlinGenerator{}
	g.GenDispatcher = gen.NewGenDispatcher(g)
	return g
}

func (g *KotlinGenerator) Generate(ctx *gen.GenCtx) (error, error) {
	g.GenCtx = ctx
	g.Warning = nil
	if ctx.GenOptions.RelativePath {
		g.warnUnavailable("relpath", "Kotlin target uses JVM packages and does not support relative imports; the option will be ignored")
	}
	if ctx.GenOptions.InnerClass {
		g.warnUnavailable("inner", "Kotlin target does not support inner generated types; the option will be ignored")
	}
	if ctx.GenOptions.SingleFile {
		g.warnUnavailable("single", "Kotlin target generates one public type per file; the option will be ignored")
	}
	if !ctx.GenOptions.MemoryCopy {
		g.Warning = definition.TopLevelWarningsJoin(g.Warning, &definition.GenerateWarning{Warning: &definition.OptionNotSetWarning{
			OptionName: "memcpy",
			Reason:     "Kotlin target uses ByteArray ownership and forces memory copying",
		}})
		ctx.GenOptions.MemoryCopy = true
	}
	var topErr definition.TopLevelError
	if err := g.AcceptGenCtx(ctx); err != nil {
		if e, ok := err.(definition.TopLevelError); ok {
			topErr = definition.TopLevelErrorsJoin(topErr, e)
		} else {
			topErr = definition.TopLevelErrorsJoin(topErr, &definition.GenerateError{Err: err})
		}
	}
	return topErr, g.Warning
}

func (g *KotlinGenerator) warnUnavailable(name, reason string) {
	g.Warning = definition.TopLevelWarningsJoin(g.Warning, &definition.GenerateWarning{Warning: &definition.OptionNotAvailableWarning{
		OptionName: name,
		Reason:     reason,
	}})
}

func kotlinPackage(unit *definition.CompilationUnit) string {
	if opt, ok := unit.Options.Get("kotlin_package"); ok {
		if lit, ok := opt.OptionValue.(*definition.StringLiteral); ok {
			return lit.StringValue
		}
		s := fmt.Sprint(opt.OptionValue)
		return strings.Trim(s, `"`)
	}
	return unit.Package.String()
}

func (g *KotlinGenerator) GenerateUnit(unit *definition.CompilationUnit) error {
	if unit.LocalTypes.Len() == 0 && gen.MatchOption(unit.Options, "omit_empty", true) {
		return nil
	}
	pkg := kotlinPackage(unit)
	imports := make([]string, 0, unit.LocalImports.Len())
	seen := map[string]bool{}
	for _, imported := range unit.LocalImports.Values() {
		p := kotlinPackage(imported)
		if p != "" && p != pkg && !seen[p] {
			imports = append(imports, "import "+p+".*")
			seen[p] = true
		}
	}
	for _, ty := range unit.LocalTypes.Values() {
		var body string
		var err error
		switch t := ty.(type) {
		case *definition.Enum:
			body, err = g.generateEnum(t)
		case *definition.Struct:
			body, err = g.generateStruct(t)
		default:
			err = fmt.Errorf("Kotlin target cannot generate type %T", ty)
		}
		if err != nil {
			return err
		}
		typeImports := append([]string(nil), imports...)
		if strings.Contains(body, ".pow(") {
			typeImports = append(typeImports, "import kotlin.math.pow")
		}
		var b strings.Builder
		b.WriteString("@file:OptIn(ExperimentalUnsignedTypes::class)\n\n")
		b.WriteString("// Target: Kotlin\n// Generated by bubbler\n// DO NOT EDIT\n")
		for _, line := range strings.Split(g.GenCtx.GenOptions.String(), "\n") {
			b.WriteString("// ")
			b.WriteString(line)
			b.WriteByte('\n')
		}
		if pkg != "" {
			b.WriteString("\npackage ")
			b.WriteString(pkg)
			b.WriteString("\n")
		}
		if len(typeImports) > 0 {
			b.WriteByte('\n')
			b.WriteString(strings.Join(typeImports, "\n"))
			b.WriteByte('\n')
		}
		b.WriteByte('\n')
		b.WriteString(body)
		b.WriteByte('\n')
		file := strings.ReplaceAll(pkg, ".", "/")
		if file != "" {
			file += "/"
		}
		file += ty.GetTypeName() + ".kt"
		if err := g.GenCtx.WriteFile(file, b.String()); err != nil {
			return err
		}
	}
	return nil
}

