func (g *KotlinGenerator) GenerateArray(a *definition.Array) (string, error) {
	switch a.ElementType.GetTypeID() {
	case definition.TypeID_Bool:
		return "BooleanArray", nil
	case definition.TypeID_Uint8:
		return "UByteArray", nil
	case definition.TypeID_Uint16:
		return "UShortArray", nil
	case definition.TypeID_Uint32:
		return "UIntArray", nil
	case definition.TypeID_Uint64:
		return "ULongArray", nil
	case definition.TypeID_Int8:
		return "ByteArray", nil
	case definition.TypeID_Int16:
		return "ShortArray", nil
	case definition.TypeID_Int32:
		return "IntArray", nil
	case definition.TypeID_Int64:
		return "LongArray", nil
	case definition.TypeID_Float32:
		return "FloatArray", nil
	case definition.TypeID_Float64:
		return "DoubleArray", nil
	default:
		elem, err := g.GenerateType(a.ElementType)
		if err != nil {
			return "", err
		}
		if a.ElementType.GetTypeID().IsEnum() {
			elem += "?"
		}
		return "Array<" + elem + ">", nil
	}
}

