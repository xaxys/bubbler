var kotlinTypeMap = map[definition.TypeID]string{
	definition.TypeID_Bool:    "Boolean",
	definition.TypeID_Uint8:   "UByte",
	definition.TypeID_Uint16:  "UShort",
	definition.TypeID_Uint32:  "UInt",
	definition.TypeID_Uint64:  "ULong",
	definition.TypeID_Int8:    "Byte",
	definition.TypeID_Int16:   "Short",
	definition.TypeID_Int32:   "Int",
	definition.TypeID_Int64:   "Long",
	definition.TypeID_Float32: "Float",
	definition.TypeID_Float64: "Double",
}

func (g *KotlinGenerator) GenerateBasicType(t *definition.BasicType) (string, error) {
	if s, ok := kotlinTypeMap[t.GetTypeID()]; ok {
		return s, nil
	}
	return "", fmt.Errorf("unknown Kotlin basic type %s", t)
}

func (g *KotlinGenerator) GenerateBasicTypeDefaultValue(t *definition.BasicType) (string, error) {
	switch t.GetTypeID() {
	case definition.TypeID_Bool:
		return "false", nil
	case definition.TypeID_Uint8, definition.TypeID_Uint16, definition.TypeID_Uint32:
		return "0u", nil
	case definition.TypeID_Uint64:
		return "0uL", nil
	case definition.TypeID_Int8, definition.TypeID_Int16, definition.TypeID_Int32:
		return "0", nil
	case definition.TypeID_Int64:
		return "0L", nil
	case definition.TypeID_Float32:
		return "0.0f", nil
	case definition.TypeID_Float64:
		return "0.0", nil
	}
	return "", fmt.Errorf("unknown Kotlin default for %s", t)
}

