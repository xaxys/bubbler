var structTemplate = `
{{- define "field" -}}
    // {{ .Pos }} {{ .Field.GetFieldKind }}: {{ .Field }}
    {{- $fieldStrLen := len .FieldStr -}}
    {{- if gt $fieldStrLen 0 }}
        {{ .FieldStr }}
    {{- end }}
{{- end -}}

{{- define "structConst" -}}
        public static bool Dynamic => {{ .StructDef.StructDynamic }};
        public static int Size => {{ calc .StructDef.StructBitSize "/" 8 }};
{{ end }}

{{- define "structDef" -}}
{{- $structDef := .StructDef -}}
{{- $fieldStrs := .FieldStrs -}}
// Struct: {{ $structDef }}
    public partial class {{ $structDef.StructName }}
    {
        {{ range $fieldStr := $fieldStrs }}
        {{ $fieldStr }}
        {{- end }}

        {{ .StructConstStr }}
        {{- range $methodStr := .MethodStrs }}
        {{ $methodStr }}
        {{- end }}
        {{ .EncoderStr }}

        {{ .DecoderStr }}

        public override bool Equals(Object obj) {
            if (this == obj) return true;
            if (obj == null) return false;
            if (this.GetType() != obj.GetType()) return false;
            {{ $structDef.StructName }} other = ({{ $structDef.StructName }})obj;
        {{- range $field := $structDef.StructFields.Values }}
        {{- if $field.GetFieldKind.IsNormal }}
            {{- $fieldName := TocamelCase $field.FieldName }}
            {{- if $field.FieldType.GetTypeID.IsBasic }}
            if (this.{{ $fieldName }} != other.{{ $fieldName }}) return false;
            {{- else if $field.FieldType.GetTypeID.IsArray }}
            if (!Enumerable.SequenceEqual(this.{{ $fieldName }}, other.{{ $fieldName }})) return false;
            {{- else }}
            if (!Object.Equals(this.{{ $fieldName }}, other.{{ $fieldName }})) return false;
            {{- end }}
        {{- end }}
        {{- end }}
            return true;
        }

        public override int GetHashCode() {
            int result = 17;
        {{- $memoryCopy := .GenOption.MemoryCopy }}
        {{- range $field := $structDef.StructFields.Values }}
        {{- if $field.GetFieldKind.IsNormal }}
            {{- $fieldName := TocamelCase $field.FieldName }}
            {{- $isBasicOrEnum := or $field.FieldType.GetTypeID.IsBasic $field.FieldType.GetTypeID.IsEnum }}
            {{- $isMemory := and $field.FieldType.GetTypeID.IsBytes ( not $memoryCopy ) }}
            {{- if and ( not $isBasicOrEnum ) ( not $isMemory ) }}
            result = result * 31 + (this.{{ $fieldName }} != null ? {{ if $field.FieldType.GetTypeID.IsArray }}this.{{ $fieldName }}.Aggregate(0, (a, v) => a * 31 + v.GetHashCode()){{ else }}this.{{ $fieldName }}.GetHashCode(){{ end }} : 0);
            {{- else }}
            result = result * 31 + {{ if $field.FieldType.GetTypeID.IsArray }}this.{{ $fieldName }}.Aggregate(0, (a, v) => a * 31 + v.GetHashCode()){{ else }}this.{{ $fieldName }}.GetHashCode(){{ end }};
            {{- end }}
        {{- end }}
        {{- end }}
            return result;
        }
    }
{{- end -}}
`

func (g CSharpGenerator) GenerateStruct(structDef *definition.Struct) (string, error) {
	name := structDef.StructName
	// check if this struct is already generated
	if g.GenTypes.Has(structDef.StructName) {
		return name, nil
	}
	// check if this struct is in generating
	if g.GenStack.Has(structDef.StructName) {
		return name, nil
	}
	// push to stack
	g.GenStack.Put(structDef.StructName, nil)
	defer g.GenStack.Remove(structDef.StructName)

	genTy, err := g.generateStruct(structDef)
	if err != nil {
		return "", err
	}
	g.GenTypes.Put(structDef.StructName, genTy)

	return name, nil
}

func (g CSharpGenerator) generateStruct(structDef *definition.Struct) (*GeneratedType, error) {
	fieldInitStrs := make([]string, structDef.StructFields.Len())
	if err := structDef.ForEachFieldWithPos(func(field definition.Field, index int, start int64, dynamic bool, pos string) error {
		fieldStr, err := g.GenerateField(field)
		if err != nil {
			return err
		}

		fieldData := map[string]any{
			"Pos":      pos,
			"Field":    field,
			"FieldStr": fieldStr,
		}

		str := util.ExecuteTemplate(structTemplate, "field", nil, fieldData)
		fieldInitStrs[index] = str
		return nil
	}); err != nil {
		return nil, err
	}

	methodStrs := []string{}
	if err := structDef.ForEachField(func(field definition.Field, index int, start int64, dynamic bool) error {
		if !field.GetFieldKind().IsNormal() {
			return nil
		}
		normalField := field.(*definition.NormalField)
		for _, group := range normalField.FieldMethods.Values() {
			methodStr, err := g.GenerateCustomGetterSetter(group)
			if err != nil {
				return err
			}
			methodStrs = append(methodStrs, methodStr)
		}

		if !g.GenCtx.GenOptions.MinimalCode {
			rawGetterStr, err := g.GenerateRawGetterSetter(field)
			if err != nil {
				return err
			}
			methodStrs = append(methodStrs, rawGetterStr)
		}
		return nil
	}); err != nil {
		return nil, err
	}

	constData := map[string]any{
		"StructDef": structDef,
	}
	constStr := util.ExecuteTemplate(structTemplate, "structConst", nil, constData)

	encoderStr, err := g.GenerateEncoder(structDef)
	if err != nil {
		return nil, err
	}

	decoderStr, err := g.GenerateDecoder(structDef)
	if err != nil {
		return nil, err
	}

	defData := map[string]any{
		"StructDef":      structDef,
		"FieldStrs":      fieldInitStrs,
		"StructConstStr": constStr,
		"MethodStrs":     methodStrs,
		"EncoderStr":     encoderStr,
		"DecoderStr":     decoderStr,
		"GenOption":      g.GenCtx.GenOptions,
	}

	defStr := util.ExecuteTemplate(structTemplate, "structDef", nil, defData)

	code := &GeneratedType{
		GeneratedDef: defStr,
	}
	return code, nil
}

