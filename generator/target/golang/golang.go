var fileTemplate = `
{{- define "file" -}}
// Target: Go
// Generated by bubbler
// DO NOT EDIT

package {{ .Package.PackageName }}

{{ $curUnit := .Unit -}}
{{ $imports := .Imports -}}
{{ if or ( gt $imports.Len 0 ) .GenState.HasUsed -}}
import (
{{- range $unit := $imports.Values -}}
{{- if $unit.Options.Has "go_package" }}
{{- $goPackage := $unit.Options.MustGet "go_package" }}
    . {{ $goPackage.OptionValue }}
{{- else }}
    . "{{ $unit.Package.ToPath "/" "" }}"
{{- end }}
{{- end }}
{{- if .GenState.HasUsed }}
{{- if .GenState.UseMath }}
    "math"
{{- end }}
{{- if .GenState.UseUnsafe }}
    "unsafe"
{{- end }}
{{- end }}
)
{{ end -}}

{{ range $entry := .GenTypes.Entries -}}
{{- $type := $entry.Value }}
/* ====================== {{ $entry.Key }} ====================== */
{{- if $type.GeneratedDef }}
{{ $type.GeneratedDef }}
{{ end }}
/* ==================== End {{ $entry.Key }} ==================== */

{{ end -}}
{{ end -}}

{{- define "singleFile" -}}
// Target: Go (Single File)
// Generated by bubbler
// DO NOT EDIT

package {{ .Package.PackageName }}

{{ $curUnit := .Unit -}}
{{- if .GenState.HasUsed -}}
import (
{{- if .GenState.UseMath }}
    "math"
{{- end }}
{{- if .GenState.UseUnsafe }}
    "unsafe"
{{- end }}
)
{{ end -}}

{{ range $genUnit := .GenUnits.Values -}}
{{ range $entry := $genUnit.GeneratedTypes.Entries -}}
{{- $type := $entry.Value }}

/* ====================== {{ $entry.Key }} ====================== */
{{- if $type.GeneratedDef }}
{{ $type.GeneratedDef }};
{{ end }}
/* ==================== End {{ $entry.Key }} ==================== */
{{ end }}
{{ end -}}

{{- end -}}
`

func (g GoGenerator) GenerateUnit(unit *definition.CompilationUnit) error {
	if unit.LocalTypes.Len() == 0 && gen.MatchOption(unit.Options, "omit_empty", true) {
		return nil
	}

	genUnit := &GeneratedUnit{
		SourceUnit:     unit,
		GeneratedTypes: util.NewOrderedMap[string, *GeneratedType](),
	}

	g.GenUnits.Put(unit.Package.String(), genUnit)

	start := g.GenTypes.Len()

	for _, type_ := range unit.LocalTypes.Values() {
		_, err := g.GenerateType(type_)
		if err != nil {
			return err
		}
	}

	end := g.GenTypes.Len()

	genTypes := g.GenTypes.Sub(start, end)
	genUnit.GeneratedTypes = genTypes

	// do not generate file if single file
	if g.GenCtx.GenOptions.SingleFile {
		return nil
	}

	imports := resolveImports(unit)

	pkg := unit.Package
	if unit.Options.Has("go_package") {
		pkgStr := fmt.Sprint(unit.Options.MustGet("go_package").OptionValue)
		pkgStr = pkgStr[1 : len(pkgStr)-1]
		pkgStrs := strings.Split(pkgStr, "/")
		pkg = definition.NewPackage(nil, pkgStrs)
	}

	fileData := map[string]any{
		"Unit":     unit,
		"Package":  pkg,
		"GenTypes": genTypes,
		"GenState": g.GenState,
		"Imports":  imports,
	}

	fileStr := util.ExecuteTemplate(fileTemplate, "file", nil, fileData)

	filePath := pkg.ToFilePath(fmt.Sprintf("/%s.bb.go", pkg.PackageName))
	err := g.GenCtx.WriteFile(filePath, fileStr)
	if err != nil {
		return err
	}

	// clear state for next unit
	g.GenState = NewGoGeneratorState()

	return nil
}

