// ==================== GenerateNormalField ====================

var normalFieldTemplate = `
{{- define "normalField" -}}
    {{- $field := .Field -}}
    {{- $fieldName := ToPascalCase .Field.FieldName -}}
    {{- $tyStr := GenerateType $field.FieldType -}}
    {{ $fieldName }} {{ $tyStr }}
{{- end -}}
`

func (g *GoGenerator) GenerateNormalField(field *definition.NormalField) (string, error) {
	name := util.ToPascalCase(field.FieldName)
	if name[0] < 'A' || name[0] > 'Z' {
		warning := &definition.CompileWarning{
			Position: field,
			Warning: &definition.NameStyleWarning{
				Name: field.FieldName,
				Msg:  "this field will be regarded as private in Go, and can only be accessed by getter and setter or methods",
			},
		}
		g.Warning = definition.TopLevelWarningsJoin(g.Warning, warning)
	}

	funcMap := template.FuncMap{
		"GenerateType": g.GenerateType,
	}

	fieldData := map[string]any{
		"Field": field,
	}

	fieldStr := util.ExecuteTemplate(normalFieldTemplate, "normalField", funcMap, fieldData)
	return fieldStr, nil
}

