// ==================== Cpp Generator ====================

type GoGeneratorState struct {
	UseUnsafe bool
	UseMath   bool
}

func NewGoGeneratorState() *GoGeneratorState {
	return &GoGeneratorState{
		UseUnsafe: false,
		UseMath:   false,
	}
}

func (g *GoGeneratorState) HasUsed() bool {
	return g.UseUnsafe || g.UseMath
}

type GoGenerator struct {
	*gen.GenDispatcher
	GenCtx   *gen.GenCtx
	GenUnits *util.OrderedMap[string, *GeneratedUnit]
	GenTypes *util.OrderedMap[string, *GeneratedType]
	GenStack *util.OrderedMap[string, any]
	GenState *GoGeneratorState
	Warning  definition.TopLevelWarning
}

func NewGoGenerator() *GoGenerator {
	generator := &GoGenerator{
		GenDispatcher: nil,
		GenUnits:      util.NewOrderedMap[string, *GeneratedUnit](),
		GenTypes:      util.NewOrderedMap[string, *GeneratedType](),
		GenStack:      util.NewOrderedMap[string, any](),
		GenState:      NewGoGeneratorState(),
		Warning:       nil,
	}
	generator.GenDispatcher = gen.NewGenDispatcher(generator)
	return generator
}

// ==================== Util ====================

func (g *GoGenerator) generateDec(value any) string {
	return fmt.Sprintf("%d", value)
}

func (g *GoGenerator) generateHex(value any) string {
	if g.GenCtx.GenOptions.DecimalNumber {
		return fmt.Sprintf("%d", value)
	}
	return fmt.Sprintf("0x%X", value)
}

func (g *GoGenerator) generateBin(value any) string {
	if g.GenCtx.GenOptions.DecimalNumber {
		return fmt.Sprintf("%d", value)
	}
	return fmt.Sprintf("0b%b", value)
}

func resolveImports(unit *definition.CompilationUnit) *util.OrderedMap[string, *definition.CompilationUnit] {
	imports := util.NewOrderedMap[string, *definition.CompilationUnit]()
	for _, localType := range unit.LocalTypes.Values() {
		structType, ok := localType.(*definition.Struct)
		if !ok {
			continue
		}

		var dfsStructType func(structTy *definition.Struct)
		dfsStructType = func(structTy *definition.Struct) {
			for _, field := range structTy.StructFields.Values() {
				normalField, ok := field.(*definition.NormalField)
				if !ok {
					continue
				}
				ty := normalField.FieldType
				if arrTy, ok := ty.(*definition.Array); ok {
					ty = arrTy.ElementType
				}
				customTy, ok := ty.(definition.CustomType)
				if !ok {
					continue
				}
				if innerStructTy, ok := customTy.(*definition.Struct); ok {
					dfsStructType(innerStructTy)
				}
				tyName := customTy.GetTypeName()
				if unit.LocalNames.Has(tyName) {
					continue
				}
				tyUnit := customTy.GetBelongs()
				imports.Put(tyUnit.Package.String(), tyUnit)
			}
		}

		dfsStructType(structType)
	}
	return imports
}

// ==================== Generate ====================

func (g *GoGenerator) Generate(ctx *gen.GenCtx) (retErr error, retWarnings error) {
	g.GenCtx = ctx
	if ctx.GenOptions.InnerClass {
		warn := &definition.GenerateWarning{
			Warning: &definition.OptionNotAvailableWarning{
				OptionName: "inner",
				Reason:     "Go target does not support inner class, the option will be ignored",
			},
		}
		g.Warning = definition.TopLevelWarningsJoin(g.Warning, warn)
	}

	var topErr definition.TopLevelError
	genErr := g.AcceptGenCtx(ctx)
	if genErr != nil {
		if _, ok := genErr.(definition.TopLevelError); !ok {
			genErr = &definition.GenerateError{
				Err: genErr,
			}
		}
		topErr = definition.TopLevelErrorsJoin(topErr, genErr.(definition.TopLevelError))
	}

	// generate single file
	if g.GenCtx.GenOptions.SingleFile && g.GenUnits.Len() > 0 {
		unit := g.GenCtx.Units[len(g.GenCtx.Units)-1]
		pkg := unit.Package
		if unit.Options.Has("go_package") {
			pkgStr := fmt.Sprint(unit.Options.MustGet("go_package").OptionValue)
			pkgStr = pkgStr[1 : len(pkgStr)-1]
			pkgStrs := strings.Split(pkgStr, "/")
			pkg = definition.NewPackage(nil, pkgStrs)
		}

		singleData := map[string]any{
			"Package":  pkg,
			"GenUnits": g.GenUnits,
			"GenState": g.GenState,
		}

		singleStr := util.ExecuteTemplate(fileTemplate, "singleFile", nil, singleData)
		err := g.GenCtx.WriteFile("", singleStr)
		if err != nil {
			topErr = definition.TopLevelErrorsJoin(topErr, &definition.GenerateError{
				Err: err,
			})
		}
	}

	return topErr, g.Warning
}

