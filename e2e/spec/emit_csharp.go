func emitCs_decodeSize(sc Scenario) string {
    var sb strings.Builder
    sb.WriteString("    /* --- decode_size boundary checks --- */\n")
    sb.WriteString("    {\n")

    var needSource bool
    var srcCase string
    for _, ds := range sc.DecodeSizeChecks {
        if ds.SourceCase != "" {
            needSource = true
            srcCase = ds.SourceCase
        }
    }
    if needSource {
        var c Case
        for _, cc := range sc.Cases {
            if cc.Name == srcCase {
                c = cc
                break
            }
        }
        fmt.Fprintf(&sb, "        var src = new %s();\n", sc.StructName)
        for _, fv := range c.Setup {
            sb.WriteString(emitCs_setNamed(sc, "src", fv.Name, fv.V, "        "))
        }
        sb.WriteString("        byte[] full = src.Encode();\n")
    }
    fmt.Fprintf(&sb, "        var probe = new %s();\n", sc.StructName)
    for _, ds := range sc.DecodeSizeChecks {
        fmt.Fprintf(&sb, "        /* %s */\n", ds.Name)
        if ds.SourceCase != "" {
            if ds.Truncate == 0 {
                sb.WriteString("        CheckEq(DecodeSizeDynamic(probe, full), full.Length, \"complete\");\n")
            } else {
                fmt.Fprintf(&sb, "        CheckEq(DecodeSizeDynamic(probe, full.Take(full.Length - %d).ToArray()), -full.Length, \"truncate %d\");\n",
                    ds.Truncate, ds.Truncate)
            }
        } else {
            fmt.Fprintf(&sb, "        CheckEq(DecodeSizeDynamic(probe, new byte[]{%s}), %d, \"%s\");\n",
                csByteList(ds.Bytes), ds.Expected, ds.Name)
        }
    }
    sb.WriteString("    }\n")
    return sb.String()
}

func emitCs_getterSetter(sc Scenario) string {
    var sb strings.Builder
    fmt.Fprintf(&sb, "// %s — %s\n", sc.StructName, sc.Notes)
    fmt.Fprintf(&sb, "{\n    current = \"%s\";\n", sc.StructName)
    for _, c := range sc.Cases {
        fmt.Fprintf(&sb, "    /* case: %s */\n", c.Name)
        sb.WriteString("    {\n")
        fmt.Fprintf(&sb, "        var s = new %s();\n", sc.StructName)
        for _, ap := range c.Accessors {
            fmt.Fprintf(&sb, "        s.%s = F64FromBits(0x%016XUL);\n", pascalCase(ap.SetterName), ap.SetArg.U)
        }
        roundTrip := false
        for _, ap := range c.Accessors {
            if !ap.NoRoundTrip {
                roundTrip = true
                break
            }
        }
        if roundTrip {
            sb.WriteString("        byte[] buf = s.Encode();\n")
            fmt.Fprintf(&sb, "        var d = new %s();\n", sc.StructName)
            fmt.Fprintf(&sb, "        Check(d.Decode(buf) == %s.Size, \"decode size\");\n", sc.StructName)
            for _, ap := range c.Accessors {
                tol := ap.Tol
                if tol <= 0 {
                    tol = 1e-9
                }
                fmt.Fprintf(&sb, "        CheckNear(d.%s, F64FromBits(0x%016XUL), %g, \"%s\");\n",
                    pascalCase(ap.GetterName), ap.Expected.U, tol, ap.GetterName)
            }
        } else {
            for _, ap := range c.Accessors {
                tol := ap.Tol
                if tol <= 0 {
                    tol = 1e-9
                }
                fmt.Fprintf(&sb, "        CheckNear(s.%s, F64FromBits(0x%016XUL), %g, \"%s\");\n",
                    pascalCase(ap.GetterName), ap.Expected.U, tol, ap.GetterName)
            }
        }
        sb.WriteString("    }\n")
    }
    sb.WriteString("}\n")
    return sb.String()
}

// ──────────────────────────────────────────────────────────────────────────
// Value formatting
// ──────────────────────────────────────────────────────────────────────────

func csBool(v Val) string {
    if v.U != 0 {
        return "true"
    }
    return "false"
}

func csValueExpr(v Val) string {
    switch v.Kind {
    case VKBool:
        return csBool(v)
    case VKU8:
        return fmt.Sprintf("(byte)%d", uint8(v.U))
    case VKU16:
        return fmt.Sprintf("(ushort)%d", uint16(v.U))
    case VKU32:
        return fmt.Sprintf("0x%XU", uint32(v.U))
    case VKU64:
        return fmt.Sprintf("0x%XUL", v.U)
    case VKI8:
        return fmt.Sprintf("(sbyte)%d", int8(v.I))
    case VKI16:
        return fmt.Sprintf("(short)%d", int16(v.I))
    case VKI32:
        return fmt.Sprintf("%d", int32(v.I))
    case VKI64:
        return fmt.Sprintf("%dL", v.I)
    case VKF32:
        return fmt.Sprintf("F32FromBits(0x%08XU)", uint32(v.U))
    case VKF64:
        return fmt.Sprintf("F64FromBits(0x%016XUL)", v.U)
    case VKEnum:
        return fmt.Sprintf("%s.%s", v.EnumType, v.EnumName)
    case VKString:
        return csStringLit(v.Str)
    }
    return ""
}

func csStringLit(s string) string {
    var sb strings.Builder
    sb.WriteByte('"')
    for _, r := range s {
        switch r {
        case '\\':
            sb.WriteString("\\\\")
        case '"':
            sb.WriteString("\\\"")
        case '\n':
            sb.WriteString("\\n")
        case '\r':
            sb.WriteString("\\r")
        case '\t':
            sb.WriteString("\\t")
        default:
            if r < 0x20 || r > 0x7E {
                if r > 0xFFFF {
                    fmt.Fprintf(&sb, "\\U%08x", r)
                } else {
                    fmt.Fprintf(&sb, "\\u%04x", r)
                }
            } else {
                sb.WriteRune(r)
            }
        }
    }
    sb.WriteByte('"')
    return sb.String()
}

func csByteList(b []byte) string {
    parts := make([]string, len(b))
    for i, v := range b {
        parts[i] = fmt.Sprintf("0x%02X", v)
    }
    return strings.Join(parts, ", ")
}

func csTempName(s string) string {
    var sb strings.Builder
    sb.WriteByte('_')
    for _, r := range s {
        if (r >= 'a' && r <= 'z') || (r >= 'A' && r <= 'Z') || (r >= '0' && r <= '9') {
            sb.WriteRune(r)
        } else {
            sb.WriteByte('_')
        }
    }
    return sb.String()
}
